nums=list(map(int,input().split()))
width=nums[1]
pattern=".|."
for i in range(1,nums[0],2):
    string=(pattern*i).center(width,'-')
    print(string)
welcome="wWELCOME"
print(welcome.center(width,'-'))
for i in range(nums[0]-2,0,-2):
    string=(pattern*i).center(width,'-')
    print(string)
